// ── Helper: resolve dot-path key ──────────────────────────────────────────────
export const resolveKey = (obj, key) => {
    const parts = key.split('.');
    let result = obj;
    for (const p of parts) {
        result = result?.[p];
        if (result === undefined) return undefined;
    }
    return result;
};
